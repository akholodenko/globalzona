		<h2><a name="ie67win" id="ie67win">Microsoft Internet Explorer 6.x and 7.x (Windows)</a></h2>

		<ol>
			<li>Select <strong>Internet Options</strong> from the <strong>Tools</strong> menu or, for Version 7, from <strong>Tools</strong> in the upper right portion of your screen.</li>

			<li>Click the <strong>Security</strong> tab.</li>
			<li>Click <strong>Custom level&#133;</strong> in <strong>Security level for this zone.</strong></li>
			<li>Scroll down to <strong>Scripting,</strong> near the bottom of the list.</li>

			<li>Under <strong>Active scripting,</strong> choose <strong>Enable.</strong></li>
			<li>Click <strong>OK</strong> to leave Security Settings. Click <strong>OK</strong> to leave
			Internet Options.</li>
		</ol>

		<h2><a name="nav9win" id="nav9win">Netscape Navigator 9.x (Windows)</a></h2>

		<ol>
			<li>From the <strong>Tools</strong> drop-down menu at the top of the browser, select <strong>Options&hellip;.</strong></li>
			<li>Select the <strong>Content</strong> section from the options at the top of the pop-up page.</li>

			<li>Check the &ldquo;Block pop-up windows,&rdquo; &ldquo;Load images automatically,&rdquo; &ldquo;Enable JavaScript&rdquo; and &ldquo;Enable Java&rdquo; boxes.</li>
			<li>Click <strong>OK.</strong></li>

		</ol>

